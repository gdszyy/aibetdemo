export const getBrandUiSkin = (schemeMeta: SchemeMeta): BrandUiSkin => {
    if (schemeMeta.scheme === 'gtb') {
        return {
            brand: 'superbet',
            mode: schemeMeta.mode,
            style: GTB_UI_STYLE,
        };
    }

    if (schemeMeta.brand === 'betano') {
        return {
            brand: 'betano',
            mode: schemeMeta.mode,
            style: schemeMeta.mode === 'light' ? BETANO_LIGHT_UI_STYLE : BETANO_UI_STYLE,
        };
    }

    if (schemeMeta.brand === 'betbus') {
        return {
            brand: 'betbus',
            mode: schemeMeta.mode,
            style: schemeMeta.mode === 'light' ? BETBUS_LIGHT_UI_STYLE : BETBUS_UI_STYLE,
        };
    }

    if (schemeMeta.brand === 'match') {
        if (schemeMeta.mode === 'light') {
            return { brand: 'match', mode: 'light', style: MATCH_LIGHT_UI_STYLE };
        }

        return {
            brand: 'match',
            mode: 'dark',
            style: MATCH_DARK_SKIN_BY_SCHEME[schemeMeta.scheme] ?? MATCH_UI_STYLE,
        };
    }

    if (schemeMeta.brand === 'glass') {
        return {
            brand: 'glass',
            mode: schemeMeta.mode,
            style:
                GLASS_SKIN_BY_SCHEME[schemeMeta.scheme] ??
                (schemeMeta.mode === 'light' ? GLASS_LIGHT_UI_STYLE : GLASS_DARK_UI_STYLE),
        };
    }

    if (schemeMeta.brand === 'cis') {
        // CIS 仅有浅色一套，恒返回 CIS_LIGHT_UI_STYLE（无 dark 常量）。
        return {
            brand: 'cis',
            mode: schemeMeta.mode,
            style: CIS_LIGHT_UI_STYLE,
        };
    }

    return {
        brand: 'superbet',
        mode: schemeMeta.mode,
        style: schemeMeta.mode === 'light' ? SUPERBET_LIGHT_UI_STYLE : SUPERBET_UI_STYLE,
    };
};

export const useBrandUiSkin = (): BrandUiSkin => {
    const schemeMeta = useSchemeMeta();

    return useMemo(() => getBrandUiSkin(schemeMeta), [schemeMeta]);
};
